package com.example.bulider;

public class EscapeTower {

	private String escapeTower;
	
	public EscapeTower() {
	}
	
	public EscapeTower(String escapeTower) {
		super();
		this.escapeTower = escapeTower;
	}




	public String getEscapeTower() {
		return escapeTower;
	}

	public void setEscapeTower(String escapeTower) {
		this.escapeTower = escapeTower;
	}

	@Override
	public String toString() {
		return "EscapeTower [escapeTower=" + escapeTower + "]";
	}
	
	
	
	
}
